        <h2 class='section-subtitle'>Dígrafos</h2>
        <p class='section-content'>
            Además de los grafemas simples que representan letras, hay una corta serie de dígrafos (grafemas
            de dos carácteres) que al juntarse generan un valor fonético distinto al de sus componentes originantes.
            Estos no se consideran letras, sino simplemente nuevas posibilidades de valor fonético a partir de
            su combinación. Pueden incluso crear una variante para un mismo valor fonético ya preexistente
            en un grafema simple. Su aplicación es universal; es decir, es indistinto si se hallan en determinada
            instancia de la palabra o rodeados de uno u otro grafema, su fonética será ejecutada siempre y sin
            mediar reglas siquiera tampoco gramaticales.
