        <h2 class='section-subtitle'>Diacríticos</h2>
        <p class='section-content'>
            El libraterrense presenta la utilización de tres signos diacríticos: el acento grave (`),
            el acento circunflejo (^) y la diéresis (¨). El primer caso corresponde a un elemento que
            es empleado meramente para cambiar la acentuación de la palabra, y se aplica exclusivamente
            para reconocer palabras compuestas y determinar el acento sobre la primera vocal del componente
            prevalente. Como tal, no modifica la fonética propiamente dicha del carácter. Ante la inexistencia
            gráfica del acentro grave sobre la "j", se aplica el circunflejo sobre esa letra, que no hace
            otra cosa que cumplir la misma funcionalidad acentuativa. En cuanto a la diéresis, se trata de
            un recurso que altera el valor fonético de la vocal que lo sufre, causando una oclusión tanto
            labial como gutural y produciendo un sonido más opaco, y en efecto un nuevo fonema.
        </p>
